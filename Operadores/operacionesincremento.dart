void main(){
  int valor = 5, valor2=10,valor3=7;

  print(valor);
  print('Valor pre-Incrementado: ${++valor}');


 
  print('Valor post-Incrementado: ${valor2++}');
  print(valor2);

  valor+4;
  print('Valor pre-Incrementado +3: ${valor}');

  //decremento
 
  print('Valor de decremento ======:');
  print(valor3);

  print('Valor pre-decrementado: ${--valor3}');

  print('Valor post-decrementado +3: ${valor3--}');
  print(valor3);

  valor3-=4;
  print('pre-decrementado -3: ${valor3}');


}